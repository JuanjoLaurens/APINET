<template>
    <div>
      
  
      <button @click="logout" class="btn-danger btn aling ml-5">Cerrar Sesi칩n</button>
    </div>
  </template>
  
  <script>
  import axios from "axios";
  
  export default {
    methods: {
      async logout() {
        try {
          // Realizar la solicitud POST a la URL de logout
          await axios.post("http://localhost:8000/api/logout", {}, {
            headers: {
              Authorization: `Bearer ${localStorage.getItem("access_token")}`,
            },
          });
  
          // Eliminar el token de acceso del localStorage
          localStorage.removeItem("access_token");
  
          // Redireccionar a la p치gina de inicio de sesi칩n u otra p치gina deseada
          this.$router.push("/login");
        } catch (error) {
          console.error(error.response.data);
        }
      },
    },
  };
  </script>
  